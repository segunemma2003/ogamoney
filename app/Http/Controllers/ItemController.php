              return response()->json([
                  "status"=>true,
                  "data"=>"item successfully created"
              ],201);
            }

            return response()->json([
              "status"=>false,
              "data"=>"failed to create item"
          ],422);
          }catch(\Exception $e){
              return response()->json([
                  "status"=>false,
                  "msg"=>"error occurred"
              ],402);
          }
    }

